const KIND_ICONS: Record<MediaKind, (p: IconProps) => React.ReactElement> = {
  image: ImageIcon,
  data: DataIcon,
  document: DocumentIcon,
  audio: AudioIcon,
  video: VideoIcon,
};

/** Returns the icon component for a given media kind (falls back to a file). */
export function KindIcon({ kind, ...props }: IconProps & { kind: MediaKind | undefined }) {
  const Component = kind ? KIND_ICONS[kind] : FileIcon;
  return <Component {...props} />;
}

